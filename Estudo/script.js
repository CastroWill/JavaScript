// == igual a 
// ==== valor igual e igual
// != não é igual
// !== não igual ou não igual
// > maior que
// < menor que
// >= maior ou igual a
// <= menor ou igual a 

console.log(3 == "3");
console.log(3 === "3");
console.log(2 != 2);
console.log(2 !== "2");
console.log(2 > 3);
console.log(2 < 3);
console.log(2 >= 2);
console.log(2 <= 2);
